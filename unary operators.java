public class Main { 
    public static void main(String[] args) { 
        int a = 18; 
        System.out.println("Value increment after expression :"+(a++)+"  Current value of a is :"+a);
        System.out.println("Value decrement after expression :"+(a--)+"   Current value of a is :"+a);
        System.out.println("Value increment before expression:"+(++a)+"   Current value of a is :"+a);
        System.out.println("Value decrement before expression:"+(--a)+"    Current value of a is :"+a);
    }
}